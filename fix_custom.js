// 3. control.html
try {
    let ctl = fs.readFileSync('control.html', 'utf8');
    if (!ctl.includes('circuit-nav')) {
        // Find navbar end to insert circuit-nav
        const navEnd = ctl.indexOf('</nav>');
        if (navEnd > -1) {
            // Wait, control.html doesn't have sections predefined in HTML, they are built by formulas.js
            // However, the user wants the section navigation
            // If control doesn't have clear sections, we can add a simple generic one or leave it
            // Let's add standard topics for control systems
            const newNav = `
    <!-- Internal Page Navigation -->
    <nav class="circuit-nav" id="circuit-nav">
        <ul>
            <li><a href="#transfer-functions">1. Transfer Functions</a></li>
            <li><a href="#time-response">2. Time Response</a></li>
            <li><a href="#stability">3. Stability</a></li>
            <li><a href="#frequency-response">4. Frequency Response</a></li>
            <li><a href="#state-space">5. State Space</a></li>
        </ul>
    </nav>`;

            ctl = ctl.substring(0, navEnd + 6) + '\n' + newNav + ctl.substring(navEnd + 6);

            // Fix main tag
            if (ctl.includes('<main id="formula-sections">')) {
                ctl = ctl.replace('<main id="formula-sections">', '<main class="circuit-main" id="formula-sections">');
            }

            fs.writeFileSync('control.html', ctl, 'utf8');
            console.log('Fixed control.html');
        }
    }
} catch (e) { console.error(e); }
