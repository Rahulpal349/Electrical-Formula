const fs = require('fs');

// 1. estimation.html
try {
    let est = fs.readFileSync('estimation.html', 'utf8');

    // Check if we need to add the standard navbar (since git checkout restored the old one)
    if (!est.includes('id="page-search"')) {
        console.log('Estimation needs standard navbar - run fix_navbar.js separately if needed, or we just replace the body');
    }

    // Replace the unit-tabs and est-layout
    const tabsStart = est.indexOf('<!-- Unit Tabs -->');
    let layoutEnd = est.indexOf('</div>', est.indexOf('<main class="est-main"'));
    // Need to find the closing div of est-layout
    if (tabsStart > -1) {
        const afterMain = est.substring(est.indexOf('</main>'));
        const divIdx = afterMain.indexOf('</div>');
        layoutEnd = est.indexOf('</main>') + divIdx + 6;

        const newNav = `    <!-- Internal Page Navigation -->
    <nav class="circuit-nav" id="circuit-nav">
        <ul>
            <li><a href="#unit-1" class="unit-tab active" data-unit="1">💰 Unit 1</a></li>
            <li><a href="#unit-2" class="unit-tab" data-unit="2">🏠 Unit 2</a></li>
            <li><a href="#unit-3" class="unit-tab" data-unit="3">🏭 Unit 3</a></li>
            <li><a href="#unit-4" class="unit-tab" data-unit="4">🏗️ Unit 4</a></li>
            <li><a href="#unit-5" class="unit-tab" data-unit="5">⚡ Unit 5</a></li>
            <li><a href="#unit-6" class="unit-tab" data-unit="6">🔧 Unit 6</a></li>
            <li><a href="#unit-7" class="unit-tab" data-unit="7">🧮 Unit 7</a></li>
        </ul>
    </nav>

    <!-- Main Content -->
    <main class="circuit-main" id="est-main">
        <!-- All unit sections generated by estimation.js -->
    </main>`;

        est = est.substring(0, tabsStart) + newNav + est.substring(layoutEnd);
        fs.writeFileSync('estimation.html', est, 'utf8');
        console.log('Fixed estimation.html');
    }
} catch (e) { console.error(e); }

